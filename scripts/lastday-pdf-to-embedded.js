function buildCounts(vals) {
  var counts = {};
  for (var i = 0; i < CODES.length; i++) {
    var v = i < vals.length ? vals[i] : 0;
    if (v > 0) counts[CODES[i]] = v;
  }
  return counts;
}

function buildByRoute(rows) {
  var byRoute = {};
  rows.forEach(function (r) {
    var route = r[0], vals = r[1];
    var depot = depotForRoute(route);
    var key = depot + '|' + route;
    byRoute[key] = { depot: depot, route: route, counts: buildCounts(vals) };
  });
  return byRoute;
}

